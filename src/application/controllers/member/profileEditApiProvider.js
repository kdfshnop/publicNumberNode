import AppApiControllerBasic from "../api" ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
创建一个apiProvider实例
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
class RestfulApi extends AppApiControllerBasic {
    constructor(req, res, next) {
        super(req, res, next) ;
        this.outputs() ;
    }
    /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    输出数据
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    async outputs() {
        try {
            this.req.body.district = JSON.parse( this.req.body.district ) ;
            this.jsonObject = await this.adf.request({ 
                "apiPath" : "member.profileEdit" , 
                "method" : "post" , 
                "contentType": "application/json",
                "data" : this.req.body 
            }) ;
            /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
            输出内容
            -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
            this.output() ;
        }
        catch(ex){
            this.next(ex) ;
        }
        /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
        结束
        -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    }
}

export default RestfulApi ;