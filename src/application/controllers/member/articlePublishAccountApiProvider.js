import AppApiControllerBasic from "../api" ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
创建一个apiProvider实例
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
class RestfulApi extends AppApiControllerBasic {
    constructor(req, res, next) {
        super(req, res, next) ;
        this.outputs() ;
    }
    /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    输出数据
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    async outputs(){
        try {
            this.jsonObject = await this.adf.request({
                "apiPath" : "member.articlePublishAccount",
                "data": this.req.query
            });
            /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
            输出内容
            -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
            this.output() ;
        }
        catch(ex){
            this.next(ex) ;
        }
    }
}
export default RestfulApi ;