import AppApiControllerBasic from "../api" ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
创建一个apiProvider实例
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/

class RestfulApi extends AppApiControllerBasic {
    constructor(req,res,next){
        super(req,res,next);
        this.mock=false ;
        this.outputs() ;
    }
    /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    输出数据
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    async outputs(){
        try{
            if(this.mock) this.jsonObject=require('../../../../mock/bindSuccess.json');
            else this.jsonObject=await this.adf.request({
                "apiPath" : "member.bindSuccess",
                "contentType" : "application/json",
                "data":this.req.body,
                "method":"post"
            });
            /*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
            输出内容
            -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
            this.output() ;
        }catch(ex){
            this.next(ex);
        }
    }
}
export default RestfulApi ;