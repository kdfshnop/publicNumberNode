import SystemApiControllerBasic from "../../system/core/controllers/api" ;
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
创建一个渲染器实例
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
class AppApiControllerBasic extends SystemApiControllerBasic {
    constructor(req, res, next) {
        super(req, res, next) ;
        if(this.req.app.locals.confs.app.dubbo) this.SOAServices = this.req.app.locals.SOAServices ;
        Object.assign(this.jsonObject, {
            "status" : 1 ,
            "message" : "" ,
            "data" : null
        }) ;        
    }    
}
/*++-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
最后将render暴露出去
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
export default AppApiControllerBasic ;
