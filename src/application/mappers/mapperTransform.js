class MapperTransfrom {
    constructor () {}
    /*++----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    将boolean值转成字符串，默认true转成"有效"，false转成"无效"
    boolean值可以写成：true|false或者1|0甚至："1" | "0"
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    static booleamToStr({ key , str = [ "Y" , ""] }) {
        let obj = {
            "key" : key ,
            "transform" : (value) => {
                let val = parseInt( value , 10 ) ;
                if(val) return str[0] ;
                else return str[1] ;
            }
        }
        return obj ;
    }
    /*++----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    将boolean值转成字符串，默认true转成"有效"，false转成"无效"
    boolean值可以写成：true|false或者1|0甚至："1" | "0"
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    static booleamStatusToStr({ key , str = [ "有效" , "无效"] }) {
        let obj = {
            "key" : key ,
            "transform" : (value) => {
                let val = parseInt( value , 10 ) ;
                if(val) return str[0] ;
                else return str[1] ;
            }
        }
        return obj ;
    }
    /*++----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    转换产品类型，yfyk => 有房有客 wkzf => 悟空找房
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------++*/
    static productTypeTranslate(key) {
        let obj = {
            "key" : key ,
            "transform" : (value) => {
                let val = value.toLowerCase() ;
                if( val === "yfyk" ) return "有房有客" ;
                else if( val === "wkzf" ) return "悟空找房" ;
            }
        }
        return obj ;
    }

}

export default MapperTransfrom
